<p><span style="color: #000000;"><strong><strong>&nbsp;</strong></strong></span></p>
<p><span style="font-weight: 400; color: #000000;">-Realtors (6% of sale price)</span></p>
<p><span style="font-weight: 400; color: #000000;">-Prepping house for sale ($1000+)</span></p>

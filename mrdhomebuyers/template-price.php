<p><span style="font-weight: 400; color: #000000;">-Appraisers ($500-$1000)</span></p>
<p><span style="font-weight: 400; color: #000000;">-Inspectors ($400-$800)</span></p>
<p><span style="font-weight: 400; color: #000000;">-Repair Addendums (1%-3%+ of sales price)</span></p>
<p><span style="font-weight: 400; color: #000000;">-Loan fees (2-4% for the buyer)</span></p>
<p><span style="color: #000000;"><strong><strong>&nbsp;</strong></strong></span></p>
<p><span style="color: #000000;"><span style="font-weight: 400;">That’s a lot of mouths to feed! If you need to sell quickly, you skip a lot of those fees, but typically sell for a </span><b>substantial discount</b><span style="font-weight: 400;"> to one of the many “We Buy Houses” investors, who low-ball you on price in exchange for a quick closing and fast cash.&nbsp;</span></span></p>
<p><span style="color: #000000;"><strong><strong>&nbsp;</strong></strong></span></p>
<p><span style="color: #000000;"><b>So what’s the answer? …Take installment payments!&nbsp;</b></span></p>
